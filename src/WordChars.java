
public class WordChars {
	
	private String word;
	private String vowelSubstring;
	private String cononentSubstring;
	public String getWord() {
		return word;
	}
	public void setWord(String word) {
		this.word = word;
	}
	public String getVowelSubstring() {
		return vowelSubstring;
	}
	public void setVowelSubstring(String vowelSubstring) {
		this.vowelSubstring = vowelSubstring;
	}
	public String getCononentSubstring() {
		return cononentSubstring;
	}
	public void setCononentSubstring(String cononentSubstring) {
		this.cononentSubstring = cononentSubstring;
	}
	
	

}
